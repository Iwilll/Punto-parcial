package poo;

public class Propiedad {
	
	private int dispArrend;
	private int dispVent;
	
	public int getDispArrend() {
		return dispArrend;
	}

	public void setDispArrend(int dispArrend) {
		this.dispArrend = dispArrend;
	}

	public int getDispVent() {
		return dispVent;
	}

	public void setDispVent(int dispVent) {
		this.dispVent = dispVent;
	}

	public Propiedad(int dispArrend, int dispVent) {
		this.dispArrend = dispArrend;
		this.dispVent = dispVent;
	}
	
}
